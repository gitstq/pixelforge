# 字符宽度（像素）
_CHAR_WIDTH = 5
# 字符高度（像素）
_CHAR_HEIGHT = 7
# 字符间距（像素）
_CHAR_SPACING = 1


def _get_char_bitmap(char: str) -> List[int]:
    """获取字符的位图数据。

    Args:
        char: 单个字符

    Returns:
        7字节的位图数据列表，如果字符不支持则返回空格的位图
    """
    code = ord(char)
    if code in _FONT_5X7:
        return _FONT_5X7[code]
    return _FONT_5X7.get(32, [0x00] * 7)  # 不支持的字符用空格代替


def measure_text(text: str, font_size: int = 1) -> Tuple[int, int]:
    """测量文本渲染后的像素尺寸。

    Args:
        text: 要测量的文本
        font_size: 字体缩放倍数

    Returns:
        (宽度, 高度) 元组
    """
    if not text:
        return (0, 0)

    font_size = max(1, font_size)
    char_w = _CHAR_WIDTH * font_size
    char_h = _CHAR_HEIGHT * font_size
    spacing = _CHAR_SPACING * font_size

    width = len(text) * (char_w + spacing) - spacing
    height = char_h

    return (width, height)


def render_text_to_pixels(
    text: str,
    font_size: int = 1,
    color: tuple = (255, 255, 255)
) -> List[List[Optional[Tuple[int, int, int, int]]]]:
    """将文本渲染为像素矩阵。

    Args:
        text: 要渲染的文本
        font_size: 字体缩放倍数（1=原始5x7像素）
        color: 前景色 (R, G, B)

    Returns:
        二维像素矩阵，每个元素为 (r, g, b, a) 或 None（透明）
    """
    if not text:
        return []

    font_size = max(1, font_size)
    r, g, b = color[:3]

    # 计算总尺寸
    text_width, text_height = measure_text(text, font_size)

    # 初始化像素矩阵（全部为None/透明）
    pixels: List[List[Optional[Tuple[int, int, int, int]]]] = [
        [None for _ in range(text_width)]
        for _ in range(text_height)
    ]

    # 逐字符渲染
    x_offset = 0
    for char in text:
        bitmap = _get_char_bitmap(char)

        for row in range(_CHAR_HEIGHT):
            byte_val = bitmap[row]
            for col in range(_CHAR_WIDTH):
                # 检查该像素是否为前景
                if byte_val & (1 << (4 - col)):
                    # 缩放像素
                    for sy in range(font_size):
                        for sx in range(font_size):
                            py = row * font_size + sy
                            px = x_offset + col * font_size + sx
                            if (0 <= py < text_height and
                                    0 <= px < text_width):
                                pixels[py][px] = (r, g, b, 255)

        x_offset += (_CHAR_WIDTH + _CHAR_SPACING) * font_size

    return pixels


def render_text(
    img: Image,
    text: str,
    x: int,
    y: int,
    font_size: int = 1,
    color: tuple = (255, 255, 255)
) -> Image:
    """在图像上渲染文本。

    Args:
        img: 输入图像
        text: 要渲染的文本
        x: 起始x坐标
        y: 起始y坐标
        font_size: 字体缩放倍数
        color: 文字颜色 (R, G, B)

    Returns:
        渲染文本后的新Image对象
    """
    result = img.copy()
    text_pixels = render_text_to_pixels(text, font_size, color)

    for ty, row in enumerate(text_pixels):
        img_y = y + ty
        if img_y < 0 or img_y >= img.height:
            continue
        for tx, pixel in enumerate(row):
            img_x = x + tx
            if img_x < 0 or img_x >= img.width:
                continue
            if pixel is not None:
                pr, pg, pb, pa = pixel
                result.pixels[img_y][img_x] = Pixel(pr, pg, pb, pa)

    return result
